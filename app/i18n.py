"""Lightweight UI translation (ko / en).

The *UI language* is separate from the analysis *output language*: the user can
read the interface in Korean while generating an English report, or vice versa.
When they start an analysis in a language different from the current UI, the app
offers to switch the whole interface (see main_window).
"""
from __future__ import annotations

_CURRENT = "ko"


def set_language(lang: str) -> None:
    global _CURRENT
    _CURRENT = "en" if lang == "en" else "ko"


def get_language() -> str:
    return _CURRENT


def tr(key: str, **kwargs) -> str:
    table = _STRINGS.get(_CURRENT, _STRINGS["ko"])
    text = table.get(key)
    if text is None:
        text = _STRINGS["ko"].get(key, key)
    return text.format(**kwargs) if kwargs else text


def tr_stage(key: str) -> str:
    """Translate an analysis progress stage key (falls back to the raw value)."""
    return tr(f"stage.{key}") if f"stage.{key}" in _STRINGS["ko"] else key


